#ifdef USE_HARDCODE_DATA
NuclearData GetNuclear(int z, int a, int) {
	// Fast in-memory lookup mode used when data table is compiled in.
	auto found = nuclear_data_map.find({z, a});
	if (found == nuclear_data_map.end()) {
		throw std::runtime_error("NuclearData not found");
	}
	return found->second;
}
#else
NuclearData GetNuclear(int z, int a, int q) {
	// Runtime file lookup mode. PathManager resolves a directory that contains
	// the requested filename across executable-relative and CMake-injected hints.
	const std::string amdc_filename = "amdc_ion_2020.txt";
	std::filesystem::path amdc_data_path =
		PathManager::DataPath(amdc_filename) / amdc_filename;
	// Open AMDC file and perform a linear scan until matching (z, a).
	// The dataset is small enough that this keeps implementation simple.
	std::ifstream fin(amdc_data_path);
	if (!fin.good()) {
		throw std::runtime_error("amdc_ion_2020.txt not found");
	}
	// skip first line
	std::string line;
	std::getline(fin, line);
	NuclearData data{-1, -1, -1.0, ""};
	while (fin.good() && (data.z != z || data.a != a)) {
		fin >> data;
	}
	// close file
	fin.close();
	if (data.z != z || data.a != a) {
		throw std::runtime_error("Search NuclearData failed");
	}
	// Ion mass correction for positive charge state q.
	if (q > 0) {
		data.mass += q * electron_mass / atomic_mass_unit;
	}
	return data;
}
#endif


double GetMass(int z, int a, int q) {
	return GetNuclear(z, a, q).mass * atomic_mass_unit;
}


double GetMassInUnit(int z, int a, int q) {
	return GetNuclear(z, a, q).mass;
}


